# string is a combination of characters in the form of sequence
str = 'this is a python class'
print(str)
print(str[0])
print(str[1])
print(str[2])
print(str[3])

# indexing
print(str[0:9])
print(str[0:15:2])
print(str[0:])
print(str[:13])
print(str[:15:3])
# str[starting index : ending index : step size]

print(str*2)
print(str*3)